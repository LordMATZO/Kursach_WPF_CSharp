);
CREATE TABLE ORDER_LIST
(
	ORDER_ID INT NOT NULL,
	GOOD_ID INT NOT NULL,
	QUANTITY INT NOT NULL,
	PRIMARY KEY(ORDER_ID,GOOD_ID)
);
CREATE TABLE TASKS
(
	TASK_ID INT NOT NULL PRIMARY KEY,
	WH_ID INT NOT NULL,
